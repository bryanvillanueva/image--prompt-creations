export function tFor(
  locale: Locale,
  key: string,
  params?: Record<string, string | number>,
): string {
  const parts = key.split(".");
  let cur: string | Dict | undefined = messages[locale];
  for (const p of parts) {
    if (cur && typeof cur === "object" && p in cur) {
      cur = (cur as Dict)[p];
    } else {
      cur = undefined;
      break;
    }
  }
  if (typeof cur !== "string") {
    if (locale !== "es") return tFor("es", key, params);
    return key;
  }
  if (params) {
    return cur.replace(/\{(\w+)\}/g, (_, k) =>
      params[k] != null ? String(params[k]) : `{${k}}`,
    );
  }
  return cur;
}
